package utils;

/**
 * The Direction enum represents the possible directions that an entity can move
 * in.
 */
public enum Direction {
    /**
     * Represents the upward direction (negative y-axis).
     */
    UP,

    /**
     * Represents the downward direction (positive y-axis).
     */
    DOWN,

    /**
     * Represents the leftward direction (negative x-axis).
     */
    LEFT,

    /**
     * Represents the rightward direction (positive x-axis).
     */
    RIGHT
}
